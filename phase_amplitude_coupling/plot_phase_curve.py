def _decode_mat_char(val):
    """Decode MATLAB strings from loadmat.

    scipy may return (a) a numpy str scalar, (b) a 1×N ``char`` vector as ``<U1``
    per column — taking only ``[0]`` would yield a single letter and break
    ``signal_type == 'LFP'`` / corner labels. Join multi-element unicode/byte arrays.
    """
    if val is None:
        return ''
    if isinstance(val, bytes):
        return val.decode('utf-8', errors='replace').strip()
    if isinstance(val, np.ndarray):
        if val.size == 0:
            return ''
        if val.dtype == object:
            return _decode_mat_char(np.asarray(val.item()))
        flat = np.ravel(val)
        if flat.size == 1:
            x = flat[0]
            if isinstance(x, bytes):
                return x.decode('utf-8', errors='replace').strip()
            return str(x).strip()
        # Per-character MATLAB char row (e.g. 'Fiber1' as 6× <U1)
        if flat.dtype.kind in ('U', 'S'):
            out = []
            for t in flat:
                if isinstance(t, bytes):
                    out.append(t.decode('utf-8', errors='replace'))
                else:
                    out.append(str(t))
            return ''.join(out).strip()
        # uint16 UTF-16 (some MATLAB char saves)
        if flat.dtype == np.uint16 and flat.size > 1:
            try:
                return flat.tobytes().decode('utf-16-le', errors='replace').replace(
                    '\x00', ''
                ).strip()
            except Exception:
                pass
        x = flat[0]
        if isinstance(x, bytes):
            return x.decode('utf-8', errors='replace').strip()
        return str(x).strip()
    return str(val).strip()


def _lfp_wavelet_is_z_scored(lfp_ephys_field):
    s = _decode_mat_char(lfp_ephys_field).lower()
    if not s:
        return False
    if s in ('lfp_z_hp', 'lfp_z'):
        return True
    if s.startswith('lfp_z_'):
        return True
    return False


def lfp_phase_curve_y_axis_unit(lfp_ephys_field):
    """Left (LFP θ waveform) and right (LFP γ band) Y-axis unit."""
    return 'a.u.' if _lfp_wavelet_is_z_scored(lfp_ephys_field) else 'mV'


