def find_all_animals(base_dir):
    """Find all animal-level folders under Process/phase_curve/."""
    base_dir = Path(base_dir)
    animals  = []
    for condition in base_dir.iterdir():
        if not condition.is_dir():
            continue
        for animal in condition.iterdir():
            if animal.is_dir():
                animals.append(animal)
    return sorted(animals)


