"""
================================================================================
Phase-Curve Plot
================================================================================

Reads PhaseCurve_*.mat files generated by compute_phase_curve.m and produces
dual-y-axis waveform/amplitude-vs-phase figures.

State (running / non_running / all_epochs) in those .mats is produced by compute_phase_curve.m
using the same strict per-sample velocity rules as compute_state_spectrogram.m (all samples
> 2 cm/s for running, all < 0.1 cm/s for rest; no mean-velocity fallback; no re-classification
in this plotting script).

  Left  y-axis : Theta waveform from bandpass-filtered raw signal (phase-triggered average,
                 5–9 Hz); fallback to theta_curve × sin(phase) if waveform field missing
  Right y-axis : Low γ amplitude (30–60 Hz); LFP purple + amber gold, Fiber blue + green

X-axis shows two full theta cycles (0–720°) when independent 2-cycle data is
available; falls back to one cycle (0–360°) otherwise.

Input:
  Process/phase_curve/{condition}/{animal}/PhaseCurve_{SignalType}_{state}.mat

Output:
  Figures/phase_curve/{condition}/{animal}/
    {animal}_{SignalType}_{state}_phase_curve.png / .pdf / .svg

Usage:
    python plot_phase_curve.py
================================================================================
"""

